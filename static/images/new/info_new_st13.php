◆こすり洗いの手順

　<br>
◇ソフトレンズ

<br>
＜１＞清潔な手でコンタクトレンズを目からはずして保存液ですすぎ、利き手と反対の手のひらの上にレンズを載せ、クリーナーや消毒剤を数滴落とす

<br>
＜２＞利き手の人さし指の腹をレンズにあて、軽く押さえながら手のひらの上でレンズを一定方向にやさしく動かし、表面を２０～３０回こする（円を描くように動かすと、コンタクトレンズが破損する恐れがある）

<br>
＜３＞ひっくり返して反対側も同じように洗う

＜４＞最後に保存液か消毒剤でよくすすぐ

　<br>
<br>
◇ハードレンズ

<br>
＜１＞清潔な手でレンズをはずし、レンズを水道水かすすぎ液ですすぐ。利き手と反対の手のひらの上に、レンズの内側を上にして載せ、クリーナーを４～５滴落とす

<br>
＜２＞利き手の人さし指の腹をコンタクトレンズの内側にあてて軽く押さえ、手のひらの上でレンズを前後左右に動かしながら泡立てるように約３０回こする

<br>
＜３＞利き手の親指、人さし指、中指の３本でコンタクトレンズを挟み、レンズの内側を親指の腹で泡立てるように優しく３０回ほどこする

<br>
＜４＞レンズを水道水かすすぎ液ですすぐ

