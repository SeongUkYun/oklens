◇レンズを使う上での主な注意

<br>
▽取り扱う前は手指を石けんで洗う

<br>
▽こすり洗いをすること

<br>
▽レンズの洗浄（すすぎ）はレンズの使用前と後に必ず行う

<br>
▽レンズケースは毎日しっかり洗い、自然乾燥させる

<br>
▽レンズケース内の消毒液は毎日新しいものに交換する

<br>
▽ソフトコンタクトレンズのケアに水道水を使用しない（ソフトレンズは水を含むため微生物が付着しやすい）

<br>
▽化粧はコンタクトレンズを装着してから行う<br>
（化粧品は石けんで落ちにくく、化粧品を扱った手でレンズを扱うと、レンズに汚れが付く）

<br>
▽定期検査を受ける
