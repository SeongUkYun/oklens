                             ◇毎日こすり洗い不可欠<br>
                             <br>
コンタクトレンズの使用者は年々増え、国民の１割を超す１５００万人以上と言われる。これに伴う目の病気も増加傾向にあり、使用者の７～１０％に発生していると推測されている。中でも細菌などによる角膜感染症は、重症化すると視力低下の要因にもなる。こすり洗いなど、毎日の適切なケアが大切だ。【下桐実雅子】

<br>
<br>
埼玉県越谷市に住む秋葉洋介さん（２９）は、ガソリンスタンドでの仕事中、急に右目が白くぼやけるようになった。職場近くのあだち眼科（同県加須市、足立和孝院長）に飛び込むと、右目の視力はメガネで矯正しても０・０１しかなく、「見えない状態だった」。角膜に傷がつき、細菌感染の可能性が高いことが分かった。抗生物質の点滴治療などを続け、視力は回復したが、角膜に濁りが残った。

　<br>
<br>
秋葉さんは「仕事柄、油を扱うが、手を洗っても油汚れは落ちにくい。その状態でレンズを扱っていた」と振り返る。それ以来、仕事ではメガネを使用している。

　<br>
<br>
足立院長は「レンズの汚れなどにより角膜の表面に傷ができると、細菌などが入り込みやすくなる。治っても視力に影響を残すことがある」と説明する。

<br>
<br>
コンタクトレンズの主流はハードレンズからソフトレンズに移り、利用者は約７割を占める。ハードレンズは目にトラブルがあると痛みを感じるが、ソフトレンズは角膜と密着しているため、まぶたの刺激を和らげ痛みを抑える効果があり、病気に気付きにくい。また、利用者は低年齢化しており、１０代や２０代のトラブルも増えている。

　<br>
<br>
コンタクトレンズは直接目に触れるため、医師の処方が必要な「高度管理医療機器」だ。角膜を覆うため、レンズの汚れや酸素不足で、角膜に傷がつきやすくなる。

　<br>
<br>
コンタクトレンズによる目の病気は、角膜の表面に小さな傷がつく「点状表層角膜症」、深くまで傷が達する「角膜浸潤」や「角膜潰瘍（かいよう）」、上まぶたの裏側（結膜）にブツブツができる「巨大乳頭結膜炎」などがある。

　<br>
<br>
近年問題になっているのが、細菌やカビなどによる角膜感染症だ。目の痛みや充血などを起こし、失明する恐れもある。中でも増えているのが、他の細菌を餌にして増殖するアカントアメーバと呼ばれる微生物によるもので、特効薬がなく治りにくいのが特徴だ。

　<br>
<br>
日本眼感染症学会などがまとめた全国調査の中間報告（０７年４月～０８年８月）では、コンタクトレンズ使用が原因と考えられる角膜感染症で入院した患者は２３３人（平均年齢２８歳）。水回りなどに存在する細菌・緑膿（りょくのう）菌やアカントアメーバが、角膜の病巣部のほか、レンズケースからも多く見つかった。<br>
<br>
 角膜感染症が増えている背景の一つに、ケア用品の進歩や多様化がある。ソフトレンズはかつては煮沸消毒が中心だったが、近年は洗浄、すすぎ、消毒、保存が１本でできる消毒剤が主流になっている。道玄坂糸井眼科医院（東京都渋谷区）の糸井素純・日本コンタクトレンズ学会常任理事は「ケア方法が簡便になることで、誤ったケアをする人が増えている。量販店には専門医がいるとは限らず、説明不足も問題」と指摘する。

　<br>
<br>
糸井さんによると、消毒剤の効果には限界があり、どのレンズでも毎日のこすり洗いが重要だという。感染の温床になるレンズケースも毎回よく洗って乾かし、３カ月に１度の交換が必要。漬けおきタイプの洗浄液でも、こすり洗いを併用しないと蓄積した汚れや化粧品の汚れは落ちないという。

　<br>
<br>
全国調査では、毎日こすり洗いをする人は１８％に過ぎず、毎日消毒する人も３０％にとどまった。２週間で交換するレンズや１日使い捨てレンズを１カ月以上使っていた人もいた。

　<br>
<br>
糸井さんは「ケアの仕方を誤解している人も少なくない。定期検査に来ないと、正しいケア方法を知る機会も減ってしまう」と話し、３カ月に１度の定期検査の受診を呼びかける。また、レンズケアに自信のない人には、１日使い捨てのレンズを薦めている。
