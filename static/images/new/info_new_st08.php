                           <td width="695" height="3" colspan="2" style=" padding:20;" ><span class="mf133_txt">オルソケラトロジー１年の長期観察結果及びその問題点

                             </span><br>
                             <br>
                             <span class="up_txt01">〇　五藤智子、岡本茂樹（幸塚眼科）、鄭　暁東、大橋裕一（愛媛大）</span>                             <br>
                             <span class="rf3_txt">目的</span>：オルソケラトロジー（Ortho - K）使用１年の長期観察においてLASIK施行者と視機能につき比較検討した。

                             <br>
                             <span class="rf3_txt">方法：</span>幸塚眼科においてOrtho - K 施行者11名22眼、LASIK施行者15名30眼につき視力、内皮密度、高次収差、及び合併症について比較した。
結果：裸眼視力、内皮密度においてOrtho - K、LASIK両群間に有意な差は認めず良好な矯正が得られた。高次収差についてはLASIK群では術前0.37±0.41術後0.52±0.24に対してOrtho - K群では施行前0.36±0.16施行後1.20±0.73と有意に増加していた。また、Ortho -K使用１年の経過観察中、特に大きな合併症もなく一人の離脱者も見られなかった。
結論：Ortho - Kにより１年にわたって良好な視力矯正が得られるも、高次収差においては有意に増加していた。経過観察中、深刻な合併症は見られなかった。<br>
<br>
 <span class="mf133_txt">１年以上観察できたオルソケラトロジー処方患者の統計                             </span><br>
 <br>
                             <span class="up_txt01">〇　前谷　悟、新矢誠人、八重垣美沙、難波　愛、平山晃章、伊達賀一、
　　 山中平吉（八丁堀眼科医院）                             </span><br>
                             <span class="rf3_txt">目的：</span>オルソケラトロジーは、夜間に酸素透過量の高い特殊な形状のハードレンズを装用して、日中は裸眼で過ごすという画期的な近視矯正方法である。本邦にも、徐々に普及されて来ているが、その長期観察結果の報告は少ない。今回我々は、処方後１年以上の長期に渡って、経過観察できた症例の経過をまとめて報告する。

                             <br>
                             <span class="rf3_txt">方法：</span>平成13年3月4日より平成16年2月28日の期間にオルソケラトロジー治療を行い、365日以上経過観察できた37人73眼について、詳細に調べた。17.7歳で、内訳は男性12人・女性25人で、平均観察期間は667.2日だった。開始時に軽度近視眼（－3.0Ｄ以内）28眼、中等度近視（－6.0Ｄ以内）33眼、強度近視（－9.75Ｄ以内）12眼であった。73眼中66眼が、治療により裸眼視力が1.0以上になり、それまでに期間は1日～373日で、平均35.7日であった。残り7眼も全て裸眼視力0.7以上見えた。軽度近視の方が早期に視力が回復した。レンズの処方交換は0～4回で、平均0.62回であった。装用を一時中止した合併症としては、表層角膜炎・アレルギー性角膜炎などがあった。全期間を通じて重篤な合併症はなく、角膜内皮細胞数が減少することもなかった。
結論：オルソケラトロジー治療は、早期に良好な裸眼視力が得られた。また、長期にわたっても、重篤な合併症もなく、安定した裸眼視力が得られるので、有効な治療方法と考えられた。　</td>
