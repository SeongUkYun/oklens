                           <td width="695" height="3" colspan="2" style="text-align:justify; padding:20;" >東京都中央区保健所は２５日、銀座眼科（同区、溝口朝雄院長）で、レーザー光線を使って近視を矯正するレーシック手術を受けた患者６３９人のうち、１８歳から６６歳の男女６７人が、感染性角膜炎や結膜炎を発症したと発表した。
２人が入院し、１９歳の女性は家族によると、失明の恐れがある重症という。<br>
ほかの患者の容体は調査中で、溝・院長は「今後、角膜移植が必要な患者が出る可能性もある」としている。


<br>
<br> 
保健所や溝口院長によると、最初の感染が見つかったのは昨年１０月。以降も感染報告が相次いだが溝・院長は治療を続け、今年２月までに６７人が感染。
同眼科が１月２０日ごろ、治療機器を消毒する機器を入れ替えたところ感染がなくなったという。
保健所は消毒機器の衛生管理や、医師の手洗いが不適切だったことなどが原因とみて調べている。


<br>
<br>溝口院長によると、同眼科は平成１８年８月に別の眼科を引き継いで開院。
消毒機器もその際に引き継ぎ、消毒やメンテナンスは一度も行っていないという。
患者からの通報を受けた保健所が２月に立ち入り調査を行うまで同眼科から報告していなかった。溝・院長は「深くおわびします」と謝罪。
報告が遅れたことについては、「試行錯誤すればよくなると思った。不徳の致すところ」と語った。
感染者に対しては治療費を支払うなどして対応するという。


　<br>
<br>
レーシック手術については、日本眼科学会がガイドラインで、講習会を受けた眼科の専門医を術者の条件としているが、溝院長は講習会などを受けてはいないという。


<br>
<br>
■松坂・ウッズも　１０年で１０倍に

　プロゴルフのタイガー・ウッズ選手や米大リーグの松坂大輔投手が受けたことで広く知られるようになったレーシック手術。「１０年間で１０倍以上になった」ともいわれるほど手術を受ける患者が増えている。 


　<br>
<br>
聖路加国際病院の山口達夫眼科部長（６２）によると、手術件数は数年前に年間５万件ほどだったが、現在は３５万件前後に増加しているとみられる。また、日本眼内レンズ屈折手術学会は年間４０万～４５万件と推定。手術可能な医療施設は国内で２００弱に上るという。自由診療のため価格競争が激化し、手術料が安くなったことなどが手術数の増加の背景とされる。


　<br>
<br>
山口部長によると、患者は２０～４０代が中心。「ドライアイなのでコンタクトが合わない」「眼鏡から解放されたい」などが動機で、手術を受けた約８５％が長期の観察でも１・０以上の視力を保っているという。 


　<br>
<br>
安全性について山口部長は、「適切な処置がされず、術後に感染症や不正乱視になる場合があるが、術中のトラブルはほとんどない」と話す。術後に重度の視力障害となった患者は１０万人に数人程度で、国内で失明例は報告されていないという。


　<br>
<br>
手術に際しては、（１）手術に適した角膜か術前に検査（２）器具や目の消毒（３）術後の感染症防止－などに注意が必要で、山口部長は「医師に十分な説明を求め、術後も手術を受けた施設でしっかりケアしてもらってほしい」と話している。<br>
<br> 
 （2009年2月26日 産経新聞）</td>
