                           <td width="695" height="3" colspan="2" style=" padding:20;" >Corneal Refractive Therapy for Hyperopia and Presbyopia
Jerome A. Legerton, OD, MS, MBA, FAAO San Diego, CA

                             <br>
                             <br>
                             The Anatomy is identical - Minor parameter difference


                             <br>
                             *Presbyopia Research Design
                             <br>
                             ・ 4 site, 5 subjects, protocol controlled<br>
・ Inclusion Criteria;
<br>
・ 45 years of age with at least +1.00 add requirement<br>
・ BSCVA of at least 0.04 logMAR(20/50) or worse at either distance or at near<br>
・ Two arms:
-Myopia up to -6.00 with up to -1.75D refractive cylinder
-Emmetropia<br>
&nbsp; (within 0.50 in each meridian)
<br>
・ Standardized examination protocol
<br>
・ Calibrated for three illumination levels<br>
・ Minimum 12 feet testing distance, high and low contrast logMAR; high contrast near at 40cm
<br>
・ Fit with conventional CRT for 1 month with target for emmetropia; at 1 month if within ±0.50 fit<br>
&nbsp;&nbsp; with investigational lenses for presbyopia<br>
・ Methods:
<br>
・ High and Low contrast distance logMAR under mesopic, photopic, high photopic; near high contrast <br>
&nbsp;&nbsp; logMAR under photopic
<br>
・ Three pupil size measures
<br>
・ Subjective and objective measures of multifocality<br>
・ Quality of life questionnaire
<br>
・ Wash out to determine return to baseline

<br>
<br>
*Primary Endpoint
<br>
・ Simultaneous Distance and near high contrast acuity ≦ 0.24 logMAR (20/32-2)
-Baseline eyes must be 0.4 logMAR (20/50) or worse at either distance or near
-3 Month post treatment 13 of 52 eyes met endpoint criteria (25%)

---Room for improvement---

<br>
<br>
